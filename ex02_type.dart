void main() {
//정수 타입
  int number1 = 10;

  //int number2 = '10';
  //int number3 = 12.5;
  print('number1 = ${number1}');
  print('-' * 30);
  //실수 타입
  double s1 = 10;
  double s2 = 10.24;
  print('s1 = ${s1}');
  print('s2 = ${s2}');

  //Boolean 타입
  bool b1 = true;
  bool b2 = false;

  print(b1);
  print('b1 = ${b1}');

  //String
  String str1 = '고길동';
  String str2 = "희동이";
  print('str1 = ' + str1);
  print('str2 = ${str2}');
}
